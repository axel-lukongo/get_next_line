#include"get_next_line.h"

/*this is a strlen white a litle modification, he count the number of char
until the \n */
size_t	ft_strlen(char *s)
{
	int	i;

	i = 0;
	while (s[i] && s[i] != '\n')
	{
		s[i++];
	}
	return (i);
}
